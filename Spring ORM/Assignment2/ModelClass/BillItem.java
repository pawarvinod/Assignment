package com.yash.model;

public class BillItem {
	int biid;
	int billid;
	int itemid;
	int qty;
	int price;
	
	public int getBiid() {
		return biid;
	}
	public void setBiid(int biid) {
		this.biid = biid;
	}
	public int getBillid() {
		return billid;
	}
	public void setBillid(int billid) {
		this.billid = billid;
	}
	public int getItemid() {
		return itemid;
	}
	public void setItemid(int itemid) {
		this.itemid = itemid;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
